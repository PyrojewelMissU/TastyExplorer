/**
 * 翻译菜品名称
 */
export const translateMealName = async (mealName) => {
  // 先查本地字典
  if (mealTranslations[mealName]) {
    return mealTranslations[mealName];
  }

  // 如果本地没有,使用API翻译
  try {
    const { translateText } = await import('./translationAPI');
    const translated = await translateText(mealName, 'en', 'zh-CN');
    return translated;
  } catch (error) {
    console.error('菜品名称翻译失败:', error);
    return mealName;
  }
};

